export function translate(locale: Language, key: string): string {
  const supplementalValue = (SUPPLEMENTAL_TRANSLATIONS[locale] as Record<string, string>)[key];
  if (supplementalValue) return supplementalValue;

  const directValue = (TRANSLATIONS[locale] as Record<string, unknown>)[key];
  if (typeof directValue === "string") return directValue;

  const parts = key.split(".");
  let value: unknown = TRANSLATIONS[locale];
  for (const part of parts) {
    if (typeof value === "object" && value !== null && part in value) {
      value = (value as Record<string, unknown>)[part];
    } else {
      value = undefined;
      break;
    }
  }
  if (typeof value === "string") return value;
  return key;
}

type LanguageContextType = {
  locale: Language;
  setLocale: (lang: Language) => void;
  t: (key: string) => string;
};

export type Translate = LanguageContextType["t"];

export const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocale] = useState<Language>(() => {
    if (typeof window !== "undefined") {
      const saved = localStorage.getItem("language") as Language | null;
      if (saved === "pt" || saved === "en") return saved;
    }
    return "pt";
  });

  const setLocaleCallback = useCallback((lang: Language) => {
    setLocale(lang);
    if (typeof window !== "undefined") {
      localStorage.setItem("language", lang);
    }
  }, []);

  const t = useCallback((key: string): string => {
    return translate(locale, key);
  }, [locale]);

  return (
    <LanguageContext.Provider value={{ locale, setLocale: setLocaleCallback, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used within LanguageProvider");
  }
  return context;
}
